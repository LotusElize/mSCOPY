const a = 10;
const b = 5;

// Addition
const sum = a + b;
console.log(`Operation addition: ${a} + ${b} = ${sum}`);
alert(`Operation addition: ${a} + ${b} = ${sum}`);

// Subtraction
const diff = a - b;
console.log(`Operation subtraction: ${a} - ${b} = ${diff}`);
alert(`Operation subtraction: ${a} - ${b} = ${diff}`);

// Multiplication
const multip = a * b;
console.log(`Operation multiplication: ${a} * ${b} = ${multip}`);
alert(`Operation multiplication: ${a} * ${b} = ${multip}`);

// Division
const division = a / b;
console.log(`Operation division: ${a} / ${b} = ${division}`);
alert(`Operation division: ${a} / ${b} = ${division}`);

// Modulus (Division Remainder)
const moddiv = a % b;
console.log(`Operation modulus (Division Remainder): ${a} % ${b} = ${moddiv}`);
alert(`Operation modulus (Division Remainder): ${a} % ${b} = ${moddiv}`);

// Exponentiation
const exponentiation = a ** b;
console.log(`Operation exponentiation): ${a} ** ${b} = ${exponentiation}`);
alert(`Operation exponentiation): ${a} ** ${b} = ${exponentiation}`);
